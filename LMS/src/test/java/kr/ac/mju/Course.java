package kr.ac.mju;

public class Course {
	private String ID;
	private String name;
	
	public Course() {
		
	}
	
	public String getID() {
		return ID;
	}
	
	public void setID(String iD) {
		ID = iD;
	}
	
	public String getname() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	

}
